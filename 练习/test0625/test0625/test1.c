#include <stdio.h>
#include <stdlib.h>
//计算一下Func1基本操作执行了多少次
void Func1(int N){
	int count = 0;
	for (int i = 0; i < N; ++i){
		for (int j = 0; j < N; ++j){
			++count;
		}
	}
	for (int k = 0; k < 2 * N; ++k){
		++count;
	}
	int M = 10;
	while (M--){
		++count;
	}
	printf("%d\n", count);
}
int main(){
	Func1(20);
	system("pause");
	return 0;
}